import type { IngredientProperty } from '@/cards/types'
import type { IngredientCard } from '@/cards/types'

export interface IngredientDefinition extends IngredientCard {
  source: 'starter' | 'exploration' | 'shop' | 'transmute' | 'preparation'
}

function ing(
  def: Omit<IngredientDefinition, 'category' | 'properties'> & {
    properties: readonly IngredientProperty[]
  },
): IngredientDefinition {
  return { ...def, category: 'ingredient' }
}

